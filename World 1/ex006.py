'''
ex006 -> Crie um algoritmo que leia um número e mostre seu dobro, seu triplo
         e sua raiz quadrada.
'''

num = int(input('Digite um número: '))

print(f'O dobro de {num} é {num*2}')
print(f'O triplo de {num} é {num*3}')
print(f'A raíz quadrada de {num} é {num**(1/2):.2f}')
